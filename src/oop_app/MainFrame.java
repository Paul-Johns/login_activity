    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String strUser = txtUsername.getText().trim();
        String strPassword = txtPassword.getText();
        
        if(strUser.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter Username");
            return;
        }
        
        if(strPassword.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter Password");
            return;
        }
        
        //Username: Administrator
        //Password: password
        
        if(strUser.equals("Administrator") && strPassword.equals("password")){
            JOptionPane.showMessageDialog(this, "Successfully Loged In!");
            return;
        } else {
            JOptionPane.showMessageDialog(this, "Login Failed, Invalid Username or Password!");         
        }
    }//GEN-LAST:event_btnLoginActionPerformed

